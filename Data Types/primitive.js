// String
let name = "Tamil";
console.log(name); // Output: Tamil

// Number
let age = 25;
let price = 19.99;
console.log(age); // Output: 25
console.log(price); // Output: 19.99

// Boolean
let isStudent = true;
let hasGraduated = false;
console.log(isStudent); // Output: true
console.log(hasGraduated); // Output: false

// Null
let emptyValue = null;
console.log(emptyValue); // Output: null

// Undefined
let something;
console.log(something); // Output: undefined

// Symbol
let sym = Symbol("unique");
console.log(sym); // Output: Symbol(unique)

// BigInt
let bigNumber = BigInt(123456789012345678901234567890);
let anotherBigNumber = 123456789012345678901234567890n;
console.log(bigNumber); // Output: 123456789012345678901234567890n
console.log(anotherBigNumber); // Output: 123456789012345678901234567890n
